CREATE DATABASE AUG17

USE AUG17

--CREATE QUERY

CREATE TABLE Deposit
(
ACTNO VARCHAR(5) PRIMARY KEY NOT NULL,
Cname VARCHAR(18) NOT NULL,
Bname VARCHAR(18) NOT NULL,
Amount INT NOT NULL,
Adate DATE NOT NULL,
FOREIGN KEY (Cname) REFERENCES CUSTOMER(CNAME),
FOREIGN KEY (Bname) REFERENCES BRANCH(Bname)
)

CREATE TABLE BRANCH
(
BNAME VARCHAR(18) PRIMARY KEY NOT NULL,
CITY VARCHAR(18) NOT NULL
)

CREATE TABLE CUSTOMER
(
CNAME VARCHAR(18) PRIMARY KEY NOT NULL,
CITY VARCHAR(18) NOT NULL
)


CREATE TABLE BORROW
(
LOANNO VARCHAR(5) PRIMARY KEY NOT NULL,
CNAME VARCHAR(18) NOT NULL,
BNAME VARCHAR(18) NOT NULL,
AMOUNT INT NOT NULL,
FOREIGN KEY (CNAME) REFERENCES CUSTOMER(CNAME),
FOREIGN KEY (BNAME) REFERENCES BRANCH(Bname)
)


