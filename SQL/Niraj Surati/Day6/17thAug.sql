CREATE DATABASE DB_BANK

USE DB_BANK

CREATE TABLE CUSTOMER(
		CName VARCHAR(18) NOT NULL CONSTRAINT PK_Cust PRIMARY KEY
	,	City VARCHAR(18) NOT NULL 
)

CREATE TABLE BRANCH(
		BName VARCHAR(18) NOT NULL  CONSTRAINT PK_Branch PRIMARY KEY
	,	City VARCHAR(18) NOT NULL 	
)

CREATE TABLE DEPOSIT(
		ACTNO VARCHAR(5) NOT NULL CONSTRAINT PK_Depo PRIMARY KEY
	,	CName VARCHAR(18) NOT NULL 
	,	BName VARCHAR(18) NOT NULL 
	,	Amount INT NOT NULL 
	,	Adate DATE NOT NULL 
	,	CONSTRAINT FK_Cust_Depo FOREIGN KEY(CName) REFERENCES CUSTOMER(CName) 
	,	CONSTRAINT FK_Branch_Depo FOREIGN KEY(BName) REFERENCES BRANCH(BName) 
)

CREATE TABLE BORROW(
		LOANNO VARCHAR(5) NOT NULL CONSTRAINT PK_Borrow PRIMARY KEY
	,	CName VARCHAR(18) NOT NULL 
	,	BName VARCHAR(18) NOT NULL 
	,	Amount INT NOT NULL 
	,	CONSTRAINT FK_Cust_Bor FOREIGN KEY(CName) REFERENCES CUSTOMER(CName) 
	,	CONSTRAINT FK_Branch_Bor FOREIGN KEY(BName) REFERENCES BRANCH(BName) 
)


