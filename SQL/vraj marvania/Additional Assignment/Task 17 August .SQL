--Q2: List All the Depositors Having Depositors Having Deposit in All the Branches where SUNIL is Having Account
   
   SELECT Cname AS [Depositors]
   FROM Deposit_Table 
   where BNAME IN (SELECT BNAME FROM Deposit_Table WHERE CNAME ='SUNIL' UNION
	               SELECT BNAME FROM BORROW_TABLE WHERE CNAME ='SUNIL')



