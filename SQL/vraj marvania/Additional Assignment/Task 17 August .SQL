CREATE TABLE Deposit_Table(
ACTNO VARCHAR(5) CONSTRAINT PK_Cnamee PRIMARY KEY NOT NULL,
Cname VARCHAR(19) CONSTRAINT fk_Cnamee FOREIGN KEY  REFERENCES  CUSTOMER_TABLE(Cname) NOT NULL,
Bname VARCHAR(18) CONSTRAINT fk_Bnamee FOREIGN KEY  REFERENCES  BRANCH_TABLE(Bname) NOT NULL,
Amount INT	NOT NULL,
Adate DATE NOT NULL
)

CREATE TABLE BORROW_TABLE(
LOANNO VARCHAR(5)  CONSTRAINT PK_LOANNO PRIMARY KEY NOT NULL,
CNAME  VARCHAR(19)  CONSTRAINT fk_CNAME FOREIGN KEY  REFERENCES CUSTOMER_TABLE(CNAME) NOT NULL,
BNAME VARCHAR(18)  CONSTRAINT fk_BNAME FOREIGN KEY  REFERENCES  BRANCH_TABLE(BNAME) NOT NULL,
AMOUNT int  NOT NULL
)

CREATE TABLE CUSTOMER_TABLE(
CNAME VARCHAR(19) CONSTRAINT PK_CNAME PRIMARY KEY NOT NULL,
CITY VARCHAR(18) NOT NULL,
)

CREATE TABLE BRANCH_TABLE(
BNAME VARCHAR(18) CONSTRAINT PK_BNAME PRIMARY KEY NOT NULL,
CITY VARCHAR(18)  NOT NULL ,
)

