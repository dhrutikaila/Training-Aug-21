/*
upDATE trigger:
gets fired when an existing record is modified

delete trigger:
gets fired when a record is deleted

After Trigger:
Fires after the execution of the DML operation for which the trigger is defined

nested Triggers:
Triggers can be nested as shown in the following figure.

Recursive Triggers:
Eventually call it self
are special cases of nested Triggers
can ne of the following type
Direct 
Indirect

Direct:  
Fires and performs an action that causes the same trigger to fire again

Indirect:
Fires a Trigger on another table and eventually the nested triggern ends up firing the first trigger again.

Creation Of Trigger
Syntax: 
create Trigger trigger_name
on {Obj name} {for| after |Instead of} {event_type DDL_Level_events}
{as {sql_statement[...n] }}
*/
