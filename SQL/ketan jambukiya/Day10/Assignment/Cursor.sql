--Using cursor implement the following task employee

--Update the salary of the employee using following condition

--Salary between 30000 and 40000 — 5000 hike

--Salary between 40000 and 55000 — 7000 hike

--Salary between 55000 and 65000 — 9000 hike


SET NOCOUNT ON
DECLARE @ID INT 
DECLARE EMP CURSOR
STATIC FOR
SELECT EMPLOYEE_ID FROM Employee
OPEN EMP
  FETCH NEXT FROM EMP INTO @ID
WHILE @@FETCH_STATUS=0
BEGIN 
UPDATE Employee SET SALARY = CASE
  WHEN SALARY BETWEEN  600000 AND 700000 THEN SALARY + 50000
  WHEN SALARY BETWEEN  700000 AND 800000 THEN SALARY + 50000
  WHEN SALARY BETWEEN  800000 AND 900000 THEN SALARY + 50000
  ELSE SALARY
  END
  WHERE EMPLOYEE_ID=@ID
  FETCH NEXT FROM EMP INTO @ID
END

CLOSE EMP
DEALLOCATE EMP
SET NOCOUNT OFF
