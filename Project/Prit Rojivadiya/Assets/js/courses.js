function addCourse(){
    var id = localStorage.getItem('id');
    var categories = document.getElementById('cate')
    var category = categories.value;
    var title = document.getElementById('heading').value;
    var url = document.getElementById('img').value;
    var price = document.getElementById('Price').value;

    console.log(category,title,url,price,id)

    var courseObj = {
        "id":id,
        "img":url,
        "heading":title,
        "price":price
    }

    console.log(courseObj);

    if(category == "javascript"){
        var a = localStorage.getItem("javascriptCourses");
        var jsArr = JSON.parse(a);
        jsArr.push(courseObj);
        console.log(jsArr);
        localStorage.setItem('javascriptCourses',JSON.stringify(jsArr));
    }else if(category == "pyhton"){
        var b = localStorage.getItem("pythonCourses");
        var pythonArr = JSON.parse(b);
        pythonArr.push(courseObj);
        console.log(pythonArr);
        localStorage.setItem('pythonCourses',JSON.stringify(pythonArr));
    }else if(category == 'web'){
        var c = localStorage.getItem("webCourses");
        var webArr = JSON.parse(c);
        webArr.push(courseObj);
        console.log(webArr);
        localStorage.setItem('webCourses',JSON.stringify(webArr));
    }

    localStorage.setItem('id',Number(localStorage.getItem('id'))+1);
}
